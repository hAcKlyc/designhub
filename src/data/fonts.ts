export function getFontPair(id: string): FontPair {
  return FONT_PAIRS.find(f => f.id === id) || FONT_PAIRS[0];
}

export function buildGoogleFontsUrl(pair: FontPair): string {
  const families = [pair.headingGoogle, pair.bodyGoogle, pair.monoGoogle]
    .filter((v, i, a) => a.indexOf(v) === i) // dedupe
    .map(f => `family=${f}`)
    .join('&');
  return `https://fonts.googleapis.com/css2?${families}&display=swap`;
}
